    t.format('[<>6]"B"0', 6.3, 'B6');
    t.format('[<>6]"B"0', -6.3, '-B6');
    t.format('[=6.3]"B"0', 6.3, 'B6');
    t.format('[=6.3]"B"0', -6.3, '-6.3');
    t.format('[>6]"A"0;[<-6]"B"0', 6.3, 'A6');
