    t.format('[>=0]"A"-0;[<=0]"B"-0;"C"-0', 6.3, 'A-6');
    t.format('[>=0]"A"-0;[<=0]"B"-0;"C"-0', -6.3, '-B-6');

    t.format('[>=100]"A"0;[<=-100]"B"0', 6.3, '######');
    t.format('[>=100]"A"0;[<=-100]"B"0', -6.3, '######');
    t.format('[>=0]"A"0;[<=-100]"B"0', -6.3, '######');
